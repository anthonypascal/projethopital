package com.company.users;

public class User {

    private String name;
    private String firstName;

    public String getName() {
        return name;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

}
